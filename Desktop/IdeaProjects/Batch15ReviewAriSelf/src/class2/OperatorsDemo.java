package class2;

public class OperatorsDemo {
    public static void main(String[] args) {

        int number=10;
        //int number=20; we can't name two boxes with same name it's not allowed

        System.out.println(number);

        int number1=10;
        int number2=20;
        System.out.println(number1+number2);//addition
        // put your cursar anywhere on a line and press ctrl+D or CMD+D duplicate it.
        System.out.println(number1-number2);//subtraction
        System.out.println(number1*number2);//multiple
        System.out.println(number1/number2);//division

        /*
        if you have to store whole numbers always us int
        if you want to store decimal numbers always use double
        if you have to store characters you can always use String
         */









    }
}
