package class7;

public class ForLoops5 {
    public static void main(String[] args) {

        for (int i = 1; i <=10; i++) {
            System.out.println("5 * "+i+" = "+(5*i));

        }
        System.out.println("************");
        for (int j = 0; j < 15; j++) {
            System.out.println("5 + "+j+" = " +(5+j));
            System.out.println(j);

        }

    }
}
